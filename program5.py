# Deklarasi variabel string
input = "Programmer Manlan"

# Menghitung jumlah karakter dengan fungsi len()
jumlah_karakter = len(input)

# Tampilkan hasil
print("Jumlah karakter menggunakan fungsi len():", jumlah_karakter)

# Pertama, kita mendeklarasikan variabel string input. Kemudian, kita menggunakan fungsi len() untuk
# menghitung jumlah karakter dari string tersebut, dan mencetak hasilnya.

# Perlu diingat bahwa Python tidak memerlukan penggunaan := untuk menetapkan nilai variabel, jadi
# kita hanya perlu menuliskan jumlah_karakter = len(input) untuk mendeklarasikan variabel
# jumlah_karakter dan menetapkan nilainya sesuai dengan hasil pemanggilan fungsi len(). Juga, Python
# tidak memiliki fungsi Count() seperti yang ada di Go, jadi kita hanya perlu menggunakan fungsi len()
# untuk menghitung jumlah karakter dari string.