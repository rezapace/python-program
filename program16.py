# data integer. Kemudian, kita menghitung luas persegi panjang dengan mengalikan panjang dengan
# lebar dan menyimpan hasilnya pada variabel luas. Terakhir, kita menampilkan hasil perhitungan
# tersebut menggunakan perintah print().