# Menghitung luas persegi

sisi = int(input("Masukkan sisi persegi: "))

luas = sisi * sisi
print("Luas persegi adalah", luas)

# Pertama, kita mendeklarasikan variabel sisi yang menyimpan sisi persegi. Kita menggunakan fungsi
# int() untuk mengkonversi inputan dari tipe data string menjadi tipe data integer. Kemudian, kita
# menghitung luas persegi dengan mengalikan sisi dengan sisi dan menyimpan hasilnya pada variabel
# luas. Terakhir, kita menampilkan hasil perhitungan tersebut menggunakan perintah print().