# menghitung luas permukaan kubus

def main():
    # Deklarasi variabel s
    s = float(input("Masukkan sisi: "))

    # Hitung luas
    luas = 6 * pow(s, 2)

    # Tampilkan hasil
    print("Luas permukaan kubus adalah", luas)

if __name__ == "__main__":
    main()

# Pada program ini, terdapat sebuah fungsi main() yang berisi logika untuk menghitung luas permukaan
# kubus. Fungsi ini pertama-tama menerima input dari pengguna berupa nilai sisi kubus. Kemudian, luas
# permukaan kubus dihitung dengan menggunakan rumus luas permukaan kubus, yaitu 6 x sisi^2.
# Setelah itu, hasil perhitungan luas permukaan ditampilkan ke layar. Pada akhir program, fungsi main()
# dipanggil agar dapat dijalanka